// 2.Area Of Circle
#include <iostream>
using namespace std;
int main()
{
    float r, pi = 3.14;
    int height;
    cout << "Enter the value of r :- ";
    cin >> r;

    cout << "Area of circle is :- " << r * r * pi << endl;
    cout << "Volume this cylinder is :-" << r * r * pi * height << endl;
    return 0;
}
